/**
 * 2.	Мамины поручения

 * Условие:

 * Мама отправила Васю купить продукты в супермаркете и забрать посылку из пункта выдачи
 * Дорога от дома до супермаркета имеет длину a метров, от дома до пункта выдачи — b метров, а дорога от супермаркета
 * до пункта выдачи — c метров. Вася может ходить по этим дорогам в любом направлении.
 * Скорость Васи зависит от того, несет ли он что-нибудь или идёт без груза. Если он не несет ни продукты, ни посылку,
 * то его скорость равна v_0 метров в минуту. Если он несет что-то одно, то его скорость равна v_1 метров в
 * минуту (v_1 ≤ v_0). Если он несет и продукты, и посылку, его скорость равна v_2 метров в минуту (v_2 ≤ v_1).
 * Вася может мгновенно купить продукты, получить посылку или занести имеющийся у него груз домой.
 * Определить минимальное время, за которое Вася может сходить за продуктами и посылкой и вернуться домой.

 * Формат ввода
 * В единственной строке ввода записаны шесть целых чисел a, b, c, v_0, v_1, v_2 — длина дороги от дома до супермаркета,
 * длина дороги от дома до пункта выдачи, длина дороги от супермаркета до пункта выдачи, скорость Васи без груза,
 * скорость Васи с продуктами либо посылкой, скорость Васи с продуктами и
 * посылкой (1 ≤ a, b, c ≤ 100; 1 ≤ v_2 ≤  v_1 ≤  v_0 ≤  100).

 * Формат вывода
 * В единственной строке выведите одно вещественное число — минимальное количество минут, которое потребуется Васе,
 * чтобы сходить за продуктами и посылкой и вернуться домой. Абсолютная или относительная погрешность
 * ответа не должна превышает 10^−4.

 * Примечание
 * В первом тесте Вася может действовать следующим образом: дойти до супермаркета, купить продукты, дойти до пункта
 * выдачи, забрать посылку, вернуться домой.
 * Суммарное время будет равно a/v_0 + c/v_1 + b/v_2 = 1/10 + 2/10 + 2/10 = 5/10.
 * Во втором тесте Вася может действовать следующим образом: дойти до пункта выдачи, забрать посылку, дойти до
 * супермаркета, купить продукты, дойти до пункта выдачи, вернуться домой. Суммарное время будет
 * равно b/v_0 + c/v_1 + c/v_2 + b/v_2 = 6/5.
 * В третьем тесте Вася может действовать следующим образом: дойти до пункта выдачи, забрать посылку, дойти до
 * супермаркета, купить продукты, вернуться домой. Суммарное время будет
 * равно b/v_0 + c/v_1 + a/v_2 = 3/7 + 4/6 + 2/5 = 314/210.
 * В четвертом тесте Вася может действовать следующим образом: дойти до магазина, дойти до почты, забрать посылку,
 * дойти до магазина, купить продукты, вернуться домой. Суммарное время будет
 * равно a/v_0 + c/v_0 + c/v_1 + a/v_2 = 1/7 + 3/7 + 3/6 + 1/5 = 267/210.
 * В пятом тесте Вася может действовать следующим образом: дойти до почты, забрать посылку, вернуться домой, оставить
 * посылку, дойти до магазина, купить продукты, вернуться домой.
 * Суммарное время будет равно b/v_0 + b/v_1 + a/v_0 + a/v_1 = 3/10 + 3/9 + 2/10 + 2/9 = 95/90.

 * Ограничение времени
 * 2 с
 * Ограничение памяти
 * 256 МБ

 * Пример 1
 * Ввод
 * 1 2 2 10 10 10
 * Вывод
 * 0.500000000000000

 * Пример 2
 * Ввод
 * 4 1 2 5 5 5
 * Вывод
 * 1.200000000000000

 * Пример 3
 * Ввод
 * 2 3 4 7 6 5
 * Вывод
 * 1.495238095238095

 * Пример 4
 * Ввод
 * 1 6 3 7 6 5
 * Вывод
 * 1.271428571428571

 * Пример 5
 * Ввод
 * 2 3 4 10 9 2
 * Вывод
 * 1.055555555555556
 */

import java.util.Arrays;
import java.util.Comparator;
import java.util.PriorityQueue;
import java.util.Scanner;

public class Main2 {
    // Состояние: позиция, грузы при себе, грузы дома
    static class State {
        int pos, carry, home;
        State(int p, int c, int h) { pos = p; carry = c; home = h; }
    }

    // Для очереди с приоритетом храним время
    static class StateTime {
        int pos, carry, home;
        double time;
        StateTime(int p, int c, int h, double t) { pos = p; carry = c; home = h; time = t; }
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = sc.nextInt();
        int v0 = sc.nextInt();
        int v1 = sc.nextInt();
        int v2 = sc.nextInt();
        sc.close();

        double[] speed = {v0, v1, v2}; // скорость по числу грузов
        double[][][] dist = new double[3][4][4]; // dist[позиция][carry][home]
        for (int i = 0; i < 3; i++)
            for (int j = 0; j < 4; j++)
                Arrays.fill(dist[i][j], Double.POSITIVE_INFINITY);
        dist[0][0][0] = 0.0;

        PriorityQueue<StateTime> pq = new PriorityQueue<>(Comparator.comparingDouble(st -> st.time));
        pq.add(new StateTime(0, 0, 0, 0.0));

        while (!pq.isEmpty()) {
            StateTime cur = pq.poll();
            int p = cur.pos;
            int carry = cur.carry;
            int home = cur.home;
            double t = cur.time;
            if (t > dist[p][carry][home] + 1e-12) continue;

            // Цель достигнута: дома и оба груза уже дома
            if (p == 0 && home == 3) {
                System.out.printf("%.10f\n", t);
                return;
            }

            int cnt = Integer.bitCount(carry);
            double sp = speed[cnt];

            // Перемещения по трём дорогам
            if (p == 0) {
                // дом -> супермаркет
                double nt = t + a / sp;
                if (nt < dist[1][carry][home] - 1e-12) {
                    dist[1][carry][home] = nt;
                    pq.add(new StateTime(1, carry, home, nt));
                }
                // дом -> пункт выдачи
                nt = t + b / sp;
                if (nt < dist[2][carry][home] - 1e-12) {
                    dist[2][carry][home] = nt;
                    pq.add(new StateTime(2, carry, home, nt));
                }
            } else if (p == 1) {
                // супермаркет -> дом
                double nt = t + a / sp;
                if (nt < dist[0][carry][home] - 1e-12) {
                    dist[0][carry][home] = nt;
                    pq.add(new StateTime(0, carry, home, nt));
                }
                // супермаркет -> пункт
                nt = t + c / sp;
                if (nt < dist[2][carry][home] - 1e-12) {
                    dist[2][carry][home] = nt;
                    pq.add(new StateTime(2, carry, home, nt));
                }
            } else { // p == 2
                // пункт -> дом
                double nt = t + b / sp;
                if (nt < dist[0][carry][home] - 1e-12) {
                    dist[0][carry][home] = nt;
                    pq.add(new StateTime(0, carry, home, nt));
                }
                // пункт -> супермаркет
                nt = t + c / sp;
                if (nt < dist[1][carry][home] - 1e-12) {
                    dist[1][carry][home] = nt;
                    pq.add(new StateTime(1, carry, home, nt));
                }
            }

            // Действия (мгновенные)
            if (p == 1 && (carry & 1) == 0 && (home & 1) == 0) {
                // взять продукты
                int nc = carry | 1;
                if (t < dist[1][nc][home] - 1e-12) {
                    dist[1][nc][home] = t;
                    pq.add(new StateTime(1, nc, home, t));
                }
            }
            if (p == 2 && (carry & 2) == 0 && (home & 2) == 0) {
                // взять посылку
                int nc = carry | 2;
                if (t < dist[2][nc][home] - 1e-12) {
                    dist[2][nc][home] = t;
                    pq.add(new StateTime(2, nc, home, t));
                }
            }
            if (p == 0) {
                // оставить все грузы дома
                int nh = home | carry;
                int nc = 0;
                if (t < dist[0][nc][nh] - 1e-12) {
                    dist[0][nc][nh] = t;
                    pq.add(new StateTime(0, nc, nh, t));
                }
            }
        }
    }
}
