/**
 * 3.	Кибербезопасность

 * Условие:

 * На работе у Васи требуют менять пароль каждую неделю, причём новый пароль не должен совпадать ни с одним из предыдущих. Любимый Васин пароль — это строка s, состоящая из строчных английских букв. Этот пароль он использует первую неделю. Придумывать и запоминать новые пароли Васе лень, поэтому он придумал правило, по которому он будет получать новый пароль каждую неделю:
 * •	Берется исходный пароль s
 * •	В данном пароле можно выбрать любую пару букв и поменять их местами
 * •	Если такой пароль не совпадает ни с одним из тех, что уже были использованы, то этот пароль можно использовать
 * Определить, сколько недель Вася сможет генерировать новые пароли с учетом пароля s на первой неделе.

 * Формат ввода
 * Вводится строка s, где (1≤∣s∣≤100000), состоящая из строчных английских букв — начальный пароль Васи.

 * Формат вывода
 * Выведите одно число — количество недель, в течение которых Вася сможет использовать исходный и измененные пароли.

 * Примечание
 * Рассмотрим первый тест:

 * Ограничение времени
 * 2 с
 * Ограничение памяти
 * 256 МБ

 * Пример 1
 * Ввод
 * abacaba
 * Вывод
 * 15

 * Пример 2
 * Ввод
 * aaaaaa
 * Вывод
 * 1
 */

import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.OutputStreamWriter;

public class Main3 {
    public static void main(String[] args) throws IOException {
        // Создаем Reader для чтения входной строки
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        // Создаем Writer для вывода результата
        BufferedWriter writer = new BufferedWriter(new OutputStreamWriter(System.out));

        // Читаем исходный пароль
        String s = reader.readLine();
        int n = s.length();               // длина строки

        // Массив для подсчета частоты каждой буквы (a-z)
        int[] cnt = new int[26];
        for (int i = 0; i < n; i++) {
            char c = s.charAt(i); // Получаем символ строки s на позиции i.
            cnt[c - 'a']++;  // Вычисляем индекс буквы в массиве ('a' → 0, 'b' → 1, …) и увеличивает соответствующий счетчик частоты.
        }

        // Общее количество пар позиций (i < j)
        long totalPairs = (long) n * (n - 1) / 2;

        // Количество пар с одинаковыми символами
        long samePairs = 0;
        for (int freq : cnt) {
            if (freq > 1) {
                // число пар для данной буквы = C(freq,2)
                samePairs += (long) freq * (freq - 1) / 2;
            }
        }

        // Результат = исходный пароль + количество различных пар с разными символами
        long result = 1 + (totalPairs - samePairs);

        // Выводим результат
        writer.write(String.valueOf(result));
        writer.newLine();                  // добавляем перевод строки (опционально)

        // Закрываем потоки
        reader.close();
        writer.close();
    }
}
