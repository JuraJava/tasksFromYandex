/**
 * 11. Мячик на лесенке

 * Условие:

 * На вершине лесенки, содержащей n ступенек, находится мячик, который начинает прыгать по ним вниз, к основанию.
 * Мячик может прыгнуть на следующую ступеньку, на ступеньку через одну или через 2. То есть, если мячик лежит на
 * 8-ой ступеньке, то он может переместиться на 5-ую, 6-ую или 7-ую.
 * Определите число всевозможных маршрутов мячика с вершины на землю.

 * Формат ввода
 * Вводится одно число  1 ≤  n  ≤ 30

 * Формат вывода
 * Выводится одно число — количество маршрутов.

 * Ограничение времени
 * 1 с
 * Ограничение памяти
 * 256 МБ

 * Пример 1
 * Ввод
 * 4
 * Вывод
 * 7

 * Пример 2
 * Ввод
 * 5
 * Вывод
 * 13
 */

import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.OutputStreamWriter;

public class Main11 {
    public static void main(String[] args) throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter writer = new BufferedWriter(new OutputStreamWriter(System.out));

        // Читаем количество ступенек
        int n = Integer.parseInt(reader.readLine());

       // reader.readLine() – читает строку текста, введенную пользователем с клавиатуры (до нажатия Enter).
       // Integer.parseInt() – преобразует эту строку в целое число (тип int).

        // Создаем массив для хранения количества маршрутов
        // dp[i] - количество способов добраться с i-й ступеньки до земли
        long[] dp = new long[n + 1];

        // Базовый случай: с земли до земли - 1 способ
        dp[0] = 1;

        // Заполняем массив для всех ступенек от 1 до n
        for (int i = 1; i <= n; i++) {
            // Начинаем со способов прыжка на 1 ступеньку вниз
            dp[i] = dp[i - 1];

            // Если можно прыгнуть на 2 ступеньки вниз
            if (i >= 2) {
                dp[i] += dp[i - 2];
            }

            // Если можно прыгнуть на 3 ступеньки вниз
            if (i >= 3) {
                dp[i] += dp[i - 3];
            }
        }

        // Выводим результат - количество способов с вершины (ступенька n)
        writer.write(String.valueOf(dp[n]));

        // writer.write(...) — записывает переданное значение в этот буфер для последующего вывода на экран.
        // В данном случае преобразует число dp[n] в строку, чтобы его можно было вывести.

        reader.close();
        writer.close();
    }

}
